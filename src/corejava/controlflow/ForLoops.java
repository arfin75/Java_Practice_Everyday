package corejava.controlflow;

public class ForLoops {

    /**
     * The for loop is a loop that lets a programmer control exactly
     * how many times a loop will iterate.
     * for (expression for initialization ; expression for testing ; expression for updating) {
     *   //body
     * }
     */

    public static void main(String[] args) {

        for (int i = 0; i<10; i++){
            // for loop iterates 10 times
            System.out.println("value of i = " + i);
        }

    }
}
